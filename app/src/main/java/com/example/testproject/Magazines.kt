@Composable
fun Magazines(magazineViewModel: MagazineViewModel) {

    LaunchedEffect(Unit) {
        magazineViewModel.getMagazines()
    }


    val magazines = magazineViewModel.magazineList
    val scrollState = rememberScrollState()




    LazyColumn(
        modifier = Modifier.fillMaxWidth(),
        contentPadding = PaddingValues(16.dp)
    ) {
        item {
            Row(
                modifier = Modifier
                    .fillMaxWidth()
                    .wrapContentHeight()
                    .padding(vertical = 25.dp),
                horizontalArrangement = Arrangement.Center,
                verticalAlignment = Alignment.CenterVertically
            ) {
                Text(
                    "Magazines :)",

                )
            }
        }
        items(magazines.component1()) { magazine ->
            Text(text = magazine.title)
        }
    }


}
